				<h2>Hassum Harrod</h2>
				<img class="pull-left img-rounded" src="images/artists/Hassum_Harrod_tn.jpg" alt="Photo of Hassum Harrod">
				<div class="media-body">
					<p>The Art College in New Dehli sponsored Hassum on scholarship for his entire undergraduate career at the university, seeing great promise in his contemporary paintings of landscapes - that use equal parts muted and vibrant tones, and are almost a contradiction in art. Expected to follow in his grandfather's and father's footsteps and become an engineer, Hassum followed his heart and never looked back. Currently, Hassum is studying for his MFA at San Francisco Art Academy, where he has become known for his impressive color theories and one-of-a-kind color palettes – and has amassed an extensive underground following.</p>
					<p>Landscapes and scenes that come to him during sleep have inspired many of Hassum's works. He's so inspired by his dreams, in fact, that he keeps a canvas and charcoal at his bedside and often begins the day sketching out the first outlines of a scene from his bed – before the vividness of his dreams begins to fade. Hassum will be speaking on "The use and absence of color in modern art" during Thursday's agenda.</p>
