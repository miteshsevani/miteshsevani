				<h2>Xhou Ta</h2>
				<img class="pull-left img-rounded" src="images/artists/Xhou_Ta_tn.jpg" alt="Photo of Xhou Ta">
				<div class="media-body">
					<p>A senior at the China International Art University, Xhou has become well-known for his miniature sculptures and paintings on rice granules, that are displayed by rear projection of microscope images on canvas. The son of a rice farmer, Xhou toiled hard on the family farm when school wasn't in session. He knew that he did not want to be a farmer like his father, but always respected his father's discipline and dedication to the trade. Painting on rice granules started as a way to pay homage to his father.</p>
					<p>As a freshman in college, Xhou became fascinated with microbiology and soon developed special tools and a technique for using microscopes and telescopic lenses to "enlarge" the painting surfaces of very small items including gravel, beads, and rice. Xhou recently expanded his techniques to create tiny sculptures, the size of a thumbnails, made of resin, clay, and other cohesive materials. Xhou will discuss the art and science behind his incredibly detailed works of art in Wednesday's seminar entitled "Beyond the Naked Eye."</p>
