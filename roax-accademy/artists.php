				<h2>Jennifer Jerome</h2>
				<img class="pull-left img-rounded" src="images/artists/Jennifer_Jerome_tn.jpg" alt="Photo of Jennifer Jerome">
				<div class="media-body">
					<p>Jennifer studied psychology as an undergraduate at UCLA, but quickly discovered her heart was in painting and drawing. Always the idealist, she decided to find a way to do both, and pursued a Master's in Art Therapy. Jennifer now works as a counselor for the developmentally disabled, using art therapy to rehabilitate young adults. She feels it fulfills her purpose on the planet. Apart from career in art therapy, Jennifer paints and draws every evening and weekend, and has amassed a large body of critically acclaimed work in a very short time.</p>
					<p>A native of New Orleans, much of Jennifer's work has centered around abstract images that depict flooding and rebuilding, having grown up as a teenager in the post-flood years. Despite the sadness of devastation and lives lost, Jennifer's work also depicts the hope and togetherness of a community that has persevered. Jennifer's exhibit, Floods of Hope, will be discussed during Tuesday's Water in Art themed sessions.</p>
